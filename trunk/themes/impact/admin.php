public function ProcessForm() {
$plugin = TImpact::Instance();
$plugin->Lock();

	if (isset($_POST['impact_colour'])) {
 				if ($this->valid_colour($_POST['impact_colour']) == 1) $plugin->color = $_POST['impact_colour'];
					}

		if (isset($_POST['impact_links'])) {
				if ($this->valid_colour($_POST['impact_links']) == 1) $plugin->links = $_POST['impact_links'];
						}

	if (isset($_POST['restore'])) {
$plugin->color = '#8A95B7'; 
$plugin->links = '#3366CC'; 
	}	

$plugin->Unlock();		
}

function valid_colour($var){
$regex = '^#([a-f]|[A-F]|[0-9]){6}^';
return preg_match($regex,$var);
}

